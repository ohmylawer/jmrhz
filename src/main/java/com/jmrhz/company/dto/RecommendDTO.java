package com.jmrhz.company.dto;

public class RecommendDTO {
	
	private int index;
	
	private String companyName;
	
	private String recommendedUnitName;
	
	private String productName;
	
	private String relationPerson;
	
	private String recommendContant;
	
	private String recommendTel;
	
	private String phone;
	
	private String remark;

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	
	

	public String getRecommendedUnitName() {
		return recommendedUnitName;
	}

	public void setRecommendedUnitName(String recommendedUnitName) {
		this.recommendedUnitName = recommendedUnitName;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getRelationPerson() {
		return relationPerson;
	}

	public void setRelationPerson(String relationPerson) {
		this.relationPerson = relationPerson;
	}
	
	public String getRecommendContant() {
		return recommendContant;
	}

	public void setRecommendContant(String recommendContant) {
		this.recommendContant = recommendContant;
	}

	public String getRecommendTel() {
		return recommendTel;
	}

	public void setRecommendTel(String recommendTel) {
		this.recommendTel = recommendTel;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
	
}
